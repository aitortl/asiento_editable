    private boolean validarFormulario() {
        boolean check = true;
        String nombre = jTFNombre.getText().trim();
        String apellidos = jTFApellidos.getText().trim();
        if (nombre.isEmpty() || apellidos.isEmpty()) {
            check = false;
        }
        return check;
    }
