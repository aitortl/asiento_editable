    private void jBReservarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBReservarActionPerformed
        if (validarFormulario()){
            String nombre=jTFNombre.getText();
            String apellidos=jTFApellidos.getText();
            Date date=new Date();
            ventanaPrincipal.reservarAsientos(nombre, apellidos, date);
            dispose();
        }else {
            jLMensajeIntroducirDatos.setText("Debes introducir el nombre y los apellidos.");
        }
    }//GEN-LAST:event_jBReservarActionPerformed

    

